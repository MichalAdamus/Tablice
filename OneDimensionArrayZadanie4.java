class OneDimensionArray{
    public static void main(String args[]){
        // deklaracja i alokacja
        double[] StudentGrades = {1.5,2.5,1,2,3};
        
        // wyświetlenie 3 ostatnich ocen
        
        System.out.println("-------------------------");

        System.out.print("Oceny z Informatyki: ");
        System.out.print(StudentGrades[3]);
        System.out.print(StudentGrades[4]);
        System.out.println();
        System.out.println("-------------------------");
		
		// Poprawienie drugiej oceny -> wstawienie wartości "5"
		
				
    }
}